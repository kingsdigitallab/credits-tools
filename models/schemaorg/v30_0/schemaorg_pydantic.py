# Important: This may not accurately match schema.org and some properties may be missing
from datetime import date, datetime
from typing import Any, Dict, List, Optional, Union
from pydantic import BaseModel, ConfigDict, Field, HttpUrl

class Thing(BaseModel):
    """Base class for structured entities containing common JSON-LD fields."""
    model_config = ConfigDict(populate_by_name=True)
    id_: Optional[str] = Field(None, alias="@id")
    type_: Optional[str] = Field(None, alias="@type")
    url: Optional[Union[HttpUrl, str]] = None
    identifier: Optional[Union[HttpUrl, str, List[Union[HttpUrl, str]]]] = Field(
        None, description="URL identifier, ideally an ORCID ID for individuals, a FundRef ID for funders, DOI for dataset / software"
    )
    description: Optional[str] = Field(None, description="A description of the item.")
    sameAs: Optional[Union[HttpUrl, List[HttpUrl]]] = Field(None, description="URL of a reference Web page that unambiguously indicates the item's identity.")
    relatedLink: Optional[Union[HttpUrl, List[HttpUrl]]] = Field(None, description="A link related to this object, e.g. related web pages")

class Organization(Thing):
    type_: str = Field("Organization", alias="@type")
    name: str = Field(..., description="The name of an Organization")
    email: Optional[str] = Field(None, description="Email address")
    address: Optional[str] = Field(None, description="Physical address of the item.")


class Person(Thing):
    type_: str = Field("Person", alias="@type")
    name: Optional[str] = Field(
        None, description="The name of a Person if separate given and family names cannot be resolved"
    )
    email: Optional[str] = Field(None, description="Email address")
    givenName: Optional[str] = Field(None, description="Given name. In the U.S., the first name of a Person.")
    familyName: Optional[str] = Field(None, description="Family name. In the U.S., the last name of an Person.")
    affiliation: Optional[Union[Organization, str, List[Union[Organization, str]]]] = Field(
        None, description="An organization that this person is affiliated with."
    )


class Review(Thing):
    type_: str = Field("Review", alias="@type")
    reviewAspect: Optional[str] = Field(None, description="This review is relevant to this part or facet of the object being review")
    reviewBody: Optional[str] = Field(None, description="The actual body of the review")


class Role(Thing):
    type_: str = Field("Role", alias="@type")
    roleName: Optional[str] = Field(None, description="A role played, performed, or filled by a person or organization.")
    startDate: Optional[Union[date, datetime]] = Field(None, description="The start date and time of the item, usually a Role.")
    endDate: Optional[Union[date, datetime]] = Field(None, description="The end date and time of the item, usually a Role.")


# Generic placeholder objects mapping onto external schemas from properties_description.csv
Agent = Union[Person, Organization, str]

