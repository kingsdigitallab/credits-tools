    operatingSystem: Optional[Union[str, List[str]]] = Field(None, description="Operating systems supported (Windows 7, OSX 10.6, Android 1.6).")
    permissions: Optional[Union[str, List[str]]] = Field(None, description="Permission(s) required to run the app.")
    processorRequirements: Optional[Union[str, List[str]]] = Field(None, description="Processor architecture required to run the application (e.g. IA64).")
    softwareHelp: Optional[Union[CreativeWork, List[CreativeWork]]] = Field(None, description="Software application help.")
    softwareRequirements: Optional[Union[SoftwareSourceCode, HttpUrl, str, List[Union[SoftwareSourceCode, HttpUrl, str]]]] = Field(None, description="Required software dependencies")
