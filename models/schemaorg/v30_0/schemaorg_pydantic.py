    storageRequirements: Optional[Union[str, HttpUrl]] = Field(None, description="Storage requirements (free space required).")
    supportingData: Optional[Union[Dict[str, Any], List[Dict[str, Any]]]] = Field(None, description="Supporting data for a SoftwareApplication.")
    releaseNotes: Optional[Union[str, HttpUrl]] = Field(None, description="Description of what changed in this version.")

